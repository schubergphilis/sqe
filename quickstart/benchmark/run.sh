#!/usr/bin/env bash
# Benchmark quickstart: generate a TPC dataset, load it into SQE as Iceberg
# tables, run the suite's queries, and report per-query timings.
#
#   ./run.sh                       # tpch at SF0.01 (default, finishes in seconds)
#   BENCH=ssb SCALE=0.1 ./run.sh   # pick a suite (tpch|ssb|tpcds) and scale factor
#   ./run.sh --down                # tear everything down
#   ./run.sh --check               # generate+load+test, then assert no failed queries
set -euo pipefail
cd "$(dirname "$0")"
. ../_shared/lib.sh

CHECK=0
for a in "$@"; do
  case "$a" in
    --down) require docker; step "tearing down"; docker compose --profile bench down -v; ok "done"; exit 0 ;;
    --check) CHECK=1 ;;
    *) die "unknown arg: $a (use --down or --check)" ;;
  esac
done

require docker
[ -f .env ] || { [ -f .env.example ] && cp .env.example .env; }
# shellcheck disable=SC1091
[ -f .env ] && { set -a; . ./.env; set +a; }

BENCH="${BENCH:-tpch}"
SCALE="${SCALE:-0.01}"
CATALOG="nessie"
DATA="/bench-data"
# SQE's Flight server always wants an auth header; the anonymous provider
# accepts any credentials and mints a token via the handshake.
AUTH=(--username anonymous --password anonymous)

step "bringing the stack up (rustfs, nessie, sqe)"
docker compose up -d --wait
ok "all services up"

# Run the one-shot sqe-bench container; deps are already up so skip them.
bench() { docker compose run --rm --no-deps sqe-bench "$@"; }

step "1/3 generate: $BENCH SF$SCALE -> shared volume $DATA"
bench generate "$BENCH" --scale "$SCALE" --output "$DATA"
ok "data generated"

step "2/3 load: CTAS-over-read_parquet into $CATALOG.${BENCH}_sf<scale>"
bench load "$BENCH" --scale "$SCALE" --data "$DATA" \
  --host sqe --port 50051 --catalog "$CATALOG" --clean "${AUTH[@]}"
ok "tables loaded"

OUT=OUTPUT.md
step "3/3 test: running the $BENCH query suite over Flight SQL (capturing to $OUT)"
RUN_LOG="$(mktemp)"
bench test "$BENCH" --scale "$SCALE" --host sqe --port 50051 --catalog "$CATALOG" "${AUTH[@]}" \
  | tee "$RUN_LOG"
{
  echo "# Captured output"
  echo
  echo "_Generated by \`BENCH=$BENCH SCALE=$SCALE ./run.sh\` on $(date -u +%Y-%m-%dT%H:%M:%SZ)._"
  echo
  echo "Suite \`$BENCH\` at scale factor \`$SCALE\`, loaded into \`$CATALOG.${BENCH}_sf...\` and"
  echo "queried over Arrow Flight SQL. Per-query timings and the machine-parseable"
  echo "\`BENCH_SUMMARY\` line (name:pass:fail:diff:skip:error:total:ms):"
  echo
  echo '```'
  # Drop the per-table load chatter and the in-container "Report written to:"
  # path (the JSON is ephemeral here -- we do not mount benchmarks/results).
  grep -vE '^\s*Loading |^\s*Done: |connecting to |Report written to:' "$RUN_LOG" || cat "$RUN_LOG"
  echo '```'
} > "$OUT"
ok "captured to $OUT"

if [ "$CHECK" -eq 1 ]; then
  step "checking invariants (asserting on the already-captured test run, no re-run)"

  # Reuse the test output captured in $RUN_LOG -- do NOT re-run the pipeline. The
  # suite prints a human summary line and a machine-parseable BENCH_SUMMARY. The
  # summary literally contains the words "fail"/"error"/"diff" (as "0 fail, 0
  # diff, ... 0 error"), so assert on the POSITIVE clean-run phrase rather than
  # assert_not_contains, which would match those zero-count tokens. We do not
  # hardcode the query count (BENCH/SCALE are env-configurable).
  run_out=$(cat "$RUN_LOG")
  assert_contains "benchmark reports a results summary" "$run_out" "pass,"
  assert_contains "no failed/diff/skip/error queries"   "$run_out" "0 fail, 0 diff, 0 skip, 0 error"
  assert_contains "machine-parseable BENCH_SUMMARY line emitted" "$run_out" "BENCH_SUMMARY:"

  rm -f "$RUN_LOG"
  check_summary
  exit 0
fi

rm -f "$RUN_LOG"
echo
ok "done. Try another suite: BENCH=ssb SCALE=0.1 ./run.sh    Tear down: ./run.sh --down"
